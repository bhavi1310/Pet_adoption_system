<h2>An ideal schedule through the eyes of a dog might be:</h2>
<section class="my-3">
  <div class ="py-2">

  <div class="container-fluid"> 
  <div class="row">
    <div class="col-md-2 col-lg-10 col-12">
<h2><em>7 am– 7:30 am</em></h2>

<ul>
<li>Wake up and go for a brisk&nbsp;walk&nbsp;with the opportunity to urinate and defecate.</li>
<li>If you have 10 minutes for a little playtime, that would be great. Fetch or something interactive is a welcome game.</li>
<li>Spend about 10 minutes of quality time just talking and petting your dog. Notice anything unusual about his demeanor, attitude, hair coat, look of his eyes etc.</li>
</ul>
<h2><em>7:30 am – 8:00 am</em></h2>
<ul>
<li>Time to eat; change the water to make sure the bowl is full and fresh.</li>
<li>Offer food if you feed your dog more than once a day.</li>
</ul>
<h2><em>8:00 am– 9:00 am</em></h2>
<ul>
<li>Dogs like to have the opportunity to go out again after they eat for the opportunity to urinate and defecate.</li>
</ul>
<h2><em>9 am – 12 pm</em></h2>
<ul>
<li>Nap time!? Or time to follow you around as you do your housework. If you are off to work – then naptime it will be.</li>
</ul>
<h2><em>12 pm – 1 pm</em></h2>

<ul>
<li>Ideally, most dogs would enjoy some interaction again with the opportunity to go out, exercise or play.</li>
</ul>
<h2><em>1 pm – 5 –pm</em></h2>
<ul>
<li>Nap time again? Or another opportunity to observe you working!</li>
</ul>
<h2><em>5 pm – 7:30 pm</em></h2>
<ul>
<li>Drinks, dinner, and playtime. Again, change the water and offer dinner. Monitor your dogs’ appetite. Offer a longer and more extensive playtime than this morning. Let him really play and burn off some of that pent-up energy.</li>
</ul>
<h2><em>7:30 pm – 11 pm</em></h2>
<ul>
<li>Quiet&nbsp;family time. This may be a good opportunity to brush and groom you dog, brush his teeth and spend some quality time, or even jump on the couch and watch some TV.</li>
</ul>
<h2><em>Before bedtime</em></h2>
<ul>
<li>Most dogs appreciate another opportunity to go out and do their “business” on last time.&nbsp;<strong><em>Bedtime!&nbsp;</em></strong>It is understood that most people have to work and may not have the flexibility to offer all the frequent trips outside. If you work really long hours, you might consider Doggie Daycare or having a pet walker, or neighbor come and gives your pet some time, attention and exercise.</li>
</ul>







