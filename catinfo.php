    <h2>How to Keep Your Cat Happy</h2>

<div class="container-fluid"> 
  <div class="row">
    <div class="col-lg-10 col-md-2 col-12">
<li class="ArticleContentBullet"><strong>Sleep</strong>&nbsp;– Cats LOVE to sleep. Cats sleep more than people, but wake much more frequently. Sleep requirements differ slightly from cat to cat, but, generally, cats like to sleep approximately 13 to 19 hours a day. How much and when they sleep depends on the level of activity in their environment. A cat living as a pet in the home is likely to sleep more than a cat that is outdoors and has to work for their food. Some cats will sleep more due to boredom.</li>
<section class="my-3">
  <div class ="py-2">
<li class="ArticleContentBullet"><strong>Play</strong>&nbsp;– Cats like to play! You can help your cat be its happiest both mentally and physically by providing plenty of stimulation. This can be in the form of toys, a companion to play with, and/or playtime with you. A laser pointer is a fun way to play with your cat, even while you are watching television. A feathery flyer is also a great toy! To better understand your kitty, readHow Cats Play</li>
<section class="my-3">
  <div class ="py-2">
<li class="ArticleContentBullet"><strong>An Interesting Environment</strong>&nbsp;– Indoor-only kitties need an interesting environment to stay mentally healthy and happy. An interesting environment includes windows that allow your cat to enjoy watching passersby or the sights and sounds of nature, such as birds at a feeder. A window perch is often ideal, as they can watch what is going on outside. An open, screened window or door and/or a screened-in porch can be very stimulating to cats as well. Opening the screened door or window in the morning is the perfect time for your cat to enjoy the sounds of the outdoors.
 Music or TV shows for cats can also be stimulating.</li>
 <section class="my-3">
  <div class ="py-2">
 <li class="ArticleContentBullet"><strong>Water</strong>&nbsp;– Cats like to have free access to fresh, clean water at all times. Change the water daily and be sure to wash out the bowl.</li>
 <section class="my-3">
  <div class ="py-2">
 <li class="ArticleContentBullet"><strong>Food</strong>&nbsp;– Generally, cats like to eat several times a day. They are nibblers and will often eat several small meals. My favorite way to feed an ideal-weight cat is to leave dry food out, so they can casually nibble, and I also offer small amounts of canned food twice daily so I can assess their appetite and behavior. Loss of appetite, orAnorexia in Cats, is one of the first symptoms of disease in many felines.</li>
 <section class="my-3">
  <div class ="py-2">
 <li class="ArticleContentBullet"><strong>A Good, Clean Litterbox</strong>&nbsp;– Cats like opportunities to urinate and defecate in a clean box that feels safe. There should be at least one litter box per cat plus one in every home. If you have two cats, you should have three litter boxes. Clear storage containers that are deep are an inexpensive favorite for cats. Use a litter that your cat likes and clean the box often. If you use scoopable litter, scoop once or twice a day depending on how many cats you have. 
Ideally, when you scoop, there should be no more than 2 or 3 spots to scoop in the box. If there is more than that, you are not scooping enough.</li>
<section class="my-3">
  <div class ="py-2">
<li class="ArticleContentBullet"><strong>General Comfort</strong>&nbsp;– Cats like the comfort of a clean environment that isn’t too hot or too cold. Most cats are very happy indoors, although some outdoor and indoor-outdoor cats also have a great life. If your cat is an outdoor cat, they should have access to warm shelter in the winter and shade in the summer with constant access to fresh, clean water. Cats also love a soft comfy bed in a high location where they can watch their environment and feel safe.</li>
<section class="my-3">
  <div class ="py-2">
<li class="ArticleContentBullet"><strong>Time</strong>&nbsp;– Cats love to be fussed over and to spend quality time with their owners. This time makes them feel important and loved, just as you would feel when a loved one gives you undivided attention. This “time” can be just sitting or watching TV. Many cats love it if you also do some petting or brushing!</li>
