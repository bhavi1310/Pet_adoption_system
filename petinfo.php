<h3 class="text-center">Ideal Daily Schedule for Dogs and Puppies</h3>
<section class="my-2">
  <div class ="py-4">
  <div class="container-fluid"> 
  <div class="row">
    <div class="col-lg-10 col-md-2 col-12">

<li class="ArticleContentBullet"><strong>Play</strong> – Dogs like to play! You can help your pet be its happiest both mentally and physically by providing plenty of stimulation. This can be in the form of toys, a companion to play with, or plenty of walks and playtime with you. If you are really busy, this could use doggie daycare or having a pet sitter stop by and give him a walk while you are at work. If a dog has enough to do during the day, he will stay awake when the sun is up and sleep at night when you do.</li>
<section class="my-3">
  <div class ="py-2">
<li class="ArticleContentBullet"><strong>Sleep</strong> – There are sleep requirement differences among the various breeds, but generally dogs like to sleep approximately 16 to 18 hours a day. How much and when they sleep depends on the level of activity in their environment. A dog living as a pet in the home is likely to sleep more than a dog that works for a living, such as a search and rescue dog or a dog working on a farm. Some dogs will sleep more due to of boredom.</li>
<section class="my-3">
  <div class ="py-2">
<li><strong><strong>Water</strong>&nbsp;– </strong>Dogs like to have free access to fresh clean water at all times. Make sure your dogs bowl is fresh. Change the food and wash out the bowl.</li>
<section class="my-3">
  <div class ="py-2">
<li><strong>Food</strong>&nbsp;– Generally, dogs like to eat once or twice a day, although some dogs will only eat well every other day. They appreciate the opportunity to eat every day. Smaller breeds often like to be fed more often than larger breed dogs. Young need to be fed more frequently than adult dogs.</li>
<section class="my-3">
  <div class ="py-2">
<li><strong>Opportunities to Eliminate</strong>&nbsp;– Adult dogs like opportunities to urinate and defecate at least every 8 hours. It might be less for some dogs. Although some dogs can hold their urine a long time, if they had their choice, they would still like the opportunity to “go out” every 6 to 8 hours. Puppies need to be let out more often, especially if you are tying to housebreak your puppy. For more information see&nbsp;House training schedules.</li>
<section class="my-3">
  <div class ="py-2">
<li><strong>General Comfort</strong>&nbsp;– Dogs like the comfort of a clean environment that is neither too hot nor too cold. Most dogs are very happy indoors although some outdoor dogs also have a great life. Dogs especially enjoy the comfort of the indoors during extreme heat or cold temperatures. If your dog is an outdoor dog, they should have access to warm shelter in the winter and shade in the summer with constant access to fresh clean water.</li>
<section class="my-3">
  <div class ="py-2">
<li><strong>Time</strong>&nbsp;– Dogs love to be fussed over and to spend quality time with their owners. This time makes them feel important and loved, just as you we feel when a loved ones gives us undivided attention. This “time” can be just sitting or watching TV. Many dogs love it if you also do some petting or brushing!</li>
<section class="my-3">
